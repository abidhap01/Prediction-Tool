    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        if(counter>0 ){
            if(counter>=1 && counter<4){
                JOptionPane.showMessageDialog(this, "you are a Novice");
                clickOnOkButtonNovice();

            }if(counter>=4 && counter<8){
                JOptionPane.showMessageDialog(this, "you are an Intermediate");
                clickOnOkButtonInter();

            }if((counter>=8 && counter<=10)){
                JOptionPane.showMessageDialog(this, "you are an Expert");
                clickOnOkButtonExpert();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Hello user!Please select answers");
            clickOnOkButton();
        }
        clearCache();
    }//GEN-LAST:event_submitButtonActionPerformed

